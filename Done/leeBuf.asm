; LEE UNA CADENA SERV 0A INT 21, ESCRIBE CARACTER A CARACTER CONTENIDO

.MODEL small
.STACK  100

;DEFINICION DE AREAS DE TRABAJO
.DATA
MSG1 DB "CADENA :$"
MSG2 DB "CADENA LEIDA: $"
MSG3 DB 10, 13, " $"
LONACT DB ?

CADENA Db 20, ?, 20 DUP ("$"), "$"



.CODE
PRINCI PROC FAR
   ;PROTOCOLO
   push ds
   sub ax,ax
   push ax
   MOV AX,@DATA
   MOV DS,AX
  

   ;INICIA PROGRAMA

    LEA DX, MSG1
    CALL MENSAJE

    RET

PRINCI ENDP

;Escribe Cadena 
MENSAJE PROC
PUSH AX
MOV AH,09H
INT 21H
POP AX
RET
MENSAJE ENDP

END PRINCI